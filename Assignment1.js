// Create four functions to print the result add, sub, div, mul
function sum(num1,num2){
    let result = num1 + num2;
    return result;
}
function sub(num1,num2){
    let result = num1 - num2;
    return result;
}
function mul(num1,num2){
    let result = num1 * num2;
    return result;
}
function div(num1,num2){
    let result = num1 / num2;
    return result;

}

s = sum(2,3);
console.log(s);

t = sub(5,3);
console.log(t)

m = mul(6,3);
console.log(m)

d = div(6,2);
console.log(d);